export function Input() {
  const [files, setFiles] = useState<Array<File>>([]);

  function handleOnChange(e: React.ChangeEvent<HTMLInputElement>) {
    const files: File[] = Array.from(e.target.files!);
    setFiles(files);
  }

  return (
    <div className="flex flex-col gap-4 items-center">
      <div className="rounded-[10px] bg-[#111111] border-2 border-[#27272A] px-5 py-3 gap-[10px] flex items-center justify-center focus-within:border-2 focus-within:border-[#1565C0] transition-colors min-w-32 hover:cursor-pointer">
        {files?.length == 0 ? (
          <UploadIcon />
        ) : files?.length == 1 ? (
          <FileIcon />
        ) : (
          <MulFileIcon />
        )}
        <label
          htmlFor="fileBtn"
          className="w-full bg-transparent outline-none hover:cursor-pointer"
        >
          {files?.length == 0
            ? "Select File(s)"
            : files?.length == 1
            ? `${files[0].name}`
            : `${files?.length} files selected`}
        </label>
        <input
          className="hidden"
          type="file"
          id="fileBtn"
          multiple
          onChange={handleOnChange}
        />
      </div>
    </div>
  );
}
